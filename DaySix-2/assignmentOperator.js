//assignment operator used to assign value from right to left
//assignment operator always work  from right left
var value=100;

//arthmatic assignment operator
value+=100;     //this is  value=value+100 //this is also shorthand operator
console.log(value);//200
value-=100;//value=value-100
console.log(value);//100
value*=100;//value=value*100
console.log(value);10000
value/=100;//value=value/100
console.log(value);//100
value%=100;//value=value%100
console.log(value);0